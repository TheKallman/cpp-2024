// Program5-1another.cpp : This file contains the 'main' function. Program execution begins and ends there.
//

#include <iostream>
using namespace std;
int main()
{
    int num = 4;

    cout << num << endl;
    cout << num++ << endl;
    cout << num << endl;
    cout << ++num << endl;
    cout << endl;
    cout << num << endl;
    cout << num-- << endl;
    cout << num << endl;
    cout << --num << endl;
}
